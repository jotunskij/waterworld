   FOR_DATE     TEXT  NOT NULL,
   TEMPERATURE  REAL  NOT NULL
);

CREATE TABLE MEASUREMENTS(
  ID            INT   PRIMARY KEY NOT NULL,
  MEASURED      TEXT  NOT NULL,
  VALUE         REAL  NOT NULL
);

